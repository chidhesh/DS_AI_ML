# Create the inventory list
inventory = ["Apples", "Bananas", "Carrots", "Dates"]

# Print the current inventory
print("Current Inventory:", inventory)

# Add new item using append()
inventory.append("Eggs")

# Remove sold out item using remove()
inventory.remove("Bananas")

# Sort the inventory alphabetically
inventory.sort()

# Print the final updated inventory
print("Final Updated Inventory:", inventory)
